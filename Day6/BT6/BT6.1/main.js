function doimau(mau) {
    var body = document.body;
    if (mau == "Green") {
        body.style.backgroundColor = "rgb(91, 183, 52)";
    }
    if (mau == "Red") {
        body.style.backgroundColor = "rgb(223, 71, 71)";
    }
    if (mau == "Pink") {
        body.style.backgroundColor = "#FFB6C1";
    }
  }